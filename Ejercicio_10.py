# Lista de muestra  
lista = ['Rojo', 'Verde', 'Blanco', 'Negro', 'Rosa', 'Amarillo']  

# Eliminar los elementos en las posiciones 0, 4 y 5  
del lista[5]  # Elimina 'Amarillo'  
